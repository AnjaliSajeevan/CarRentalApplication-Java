    private void txtYearKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtYearKeyPressed
        // TODO add your handling code here:
        char c =evt.getKeyChar();
        if ((c >= '0' && c <= '9' )||(c == KeyEvent.VK_BACK_SPACE)||(c == KeyEvent.VK_DELETE)){
            txtYear.setEditable(true);
        }else{
            txtYear.setEditable(false);
            JOptionPane.showMessageDialog(null, "Please enter only numbers !");
        }
    }//GEN-LAST:event_txtYearKeyPressed

    private void txtMakeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMakeKeyPressed
        // TODO add your handling code here:
        char c =evt.getKeyChar();
        if (!(c >= '0' && c <= '9' || c == '/')||(c == KeyEvent.VK_BACK_SPACE)||(c == KeyEvent.VK_DELETE)){
            txtMake.setEditable(true);
        }else{
            txtMake.setEditable(false);
            JOptionPane.showMessageDialog(null, "Please enter only characters !");
        }
    }//GEN-LAST:event_txtMakeKeyPressed

    private void txtExpiredKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtExpiredKeyPressed
        // TODO add your handling code here:
        char c =evt.getKeyChar();
        if ((c == 'Y' || c =='e'|| c=='s'||c=='N'||c=='o' )||(c == KeyEvent.VK_BACK_SPACE)||(c == KeyEvent.VK_DELETE)){
            txtExpired.setEditable(true);
        }else{
            txtExpired.setEditable(false);
            JOptionPane.showMessageDialog(null, "Please enter Yes or No only !");
        }
    }//GEN-LAST:event_txtExpiredKeyPressed

