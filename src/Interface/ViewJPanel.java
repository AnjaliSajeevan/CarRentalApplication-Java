    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
                Date date1;
                String make = txtMake.getText();
                String model = txtModel.getText();
                String makeYear = txtYear.getText();
                String city = txtCity.getText();
                String  serialNo = txtSerialNo.getText();
                String  expired = txtExpired.getText();
                String  available = txtAvailability.getText();
                
                String updatedOn = txtupdatedOn.getText();
                
                uber.setManufactuer(make);
                uber.setModel(model);
                uber.setMakeYear(makeYear);
                uber.setCity(city);
                uber.setSerialNo(serialNo);
                uber.setAvailability(available);
                uber.setExpired(expired);
               
                
                try {  
                     date1 = new SimpleDateFormat("MM/dd/yyyy").parse(updatedOn);
                     uber.setUpdatedDate(date1);
                     
            } catch (ParseException ex) {
                Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
         
              
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
         txtMake.setEnabled(false);
        txtModel.setEnabled(false);
        txtYear.setEnabled(false);
        txtCity.setEnabled(false);
        txtSerialNo.setEnabled(false);
        txtAvailability.setEnabled(false);
        txtExpired.setEnabled(false);
        txtupdatedOn.setEnabled(false);

       JOptionPane.showMessageDialog(null, "Updated successfully");
        
    }//GEN-LAST:event_btnSaveActionPerformed

